func Qk15(f Function, a, b float64) (result, abserr, resabs, resasc float64) {
	n := 8
	fv1 := make([]float64, n)
	fv2 := make([]float64, n)
	result, abserr, resabs, resasc = Qk(n, xgk15, wg15, wgk15, fv1, fv2, f, a, b)
	return
}

